import React, { Component } from 'react';
import Cell from './Cell'


export default class Matrix extends Component {
  

  genRow = (vals) => {
   return vals.map((val,key) => 
      <Cell 
      value={val}
      key={key}
    />
    )
  }
    
  
  genMatrix = () => this.props.values.map((rowVals,key) => <div key={key} className="row">{this.genRow(rowVals)}</div>)

  render() {
    return (
      <div id="matrix">
        {this.genMatrix()}
      </div>
    )
  }

}

